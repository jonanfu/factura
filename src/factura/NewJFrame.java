    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        persona.setNumeroFactura(jTextField1.getText());
        persona.setNombreCliente(jTextField2.getText());
        persona.setDireccion(jTextField3.getText());
        persona.setFecha(jTextField4.getText());
        persona.setIdentificacion(jTextField5.getText());
        int cant1 = Integer.parseInt(c1.getText());
        double val1 = Double.parseDouble(v1.getText());
         pr1 = new DatosProductos(cant1,d1.getText(),val1);
        cant1 = Integer.parseInt(c2.getText());
        val1 = Double.parseDouble(v2.getText());
         pr2 = new DatosProductos(cant1,d1.getText(),val1);
         cant1 = Integer.parseInt(c3.getText());
        val1 = Double.parseDouble(v3.getText());
        pr3 = new DatosProductos(cant1,d1.getText(),val1);
         
        t1.setText(pr1.valorTotal());
        t2.setText(pr2.valorTotal());
        t3.setText(pr2.valorTotal());
        double s1, s2, s3;
        s1 = Double.parseDouble(t1.getText());
        s2 = Double.parseDouble(t2.getText());
        s3 = Double.parseDouble(t3.getText());
        double st = s1 + s2 + s3;
        String t = String.valueOf(st);
        subtotal.setText(t);
        double i = st * 0.12;
        String ivaMostrar = String.valueOf(i);
        
        iva.setText(ivaMostrar);
        double sumarTotal = i + st;
       
        String mostrarTotal = String.valueOf(sumarTotal);
        total.setText(mostrarTotal);
        
        
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        
        c1.setText("");
        c2.setText("");
        c3.setText("");
        
        d1.setText("");
        d2.setText("");
        d3.setText("");
        
        v1.setText("");
        v2.setText("");
        v3.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

